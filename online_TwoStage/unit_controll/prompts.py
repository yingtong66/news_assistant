# 冷启动（无偏好摘要）时使用的固定引导语
GUIDANCE_COLD_TEMPLATE = "您好！我可以帮您设置内容过滤和推荐偏好。请告诉我您最近希望多看哪类内容，或者想屏蔽哪类内容？"

# 有偏好摘要时，让 LLM 生成"偏好说明 + 引导问句"的 prompt
GUIDANCE_WARM_PROMPT = """根据分析，该用户的内容偏好如下：
{preference_summary}

请生成一段自然的开场白，分两部分：
1. 先简要告知用户"根据您的历史浏览，分析出您对xxx感兴趣"（用自然语言概括偏好，不要照抄原文）
2. 再提一个引导问题，帮助用户进一步明确他们想看或不想看的具体内容

要求：语气友好自然，总长度不超过80字，只输出这段话本身。"""
