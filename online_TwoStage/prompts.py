# 两阶段重排的 prompt 常量

FILTERING_PROMPT = """你是一个内容过滤智能体。你的任务是根据用户的过滤规则，从候选内容列表中移除明确违反规则的内容。

## 用户过滤规则（不想看的内容）
{negative_group}

## 候选内容列表
{items}

## 要求
1. 仅移除**明确**违反用户规则的候选内容。如果不确定，保留该内容。
2. 保守过滤：保留的内容数量必须 >= 原始数量的 70%。
3. 对于每个被移除的内容，说明违反了哪条规则。

## 输出格式
严格输出如下 JSON，不要输出其他内容：
```json
{{
  "filtered_list": [
    {{"id": "xxx", "title": "xxx"}},
    ...
  ],
  "removed_list": [
    {{"id": "xxx", "title": "xxx", "reason": "违反规则：xxx"}},
    ...
  ]
}}
```"""

RERANKING_PROMPT = """你是一个内容重排智能体。你的任务是根据用户的偏好，对候选内容列表进行重新排序。

## 用户偏好信息
{positive_group}

## 候选内容列表
{items}

## 要求
1. 将与用户偏好更相关的内容排在前面。
2. **不能删除任何候选内容**，所有输入的内容都必须出现在输出中。
3. 如果没有明确的用户偏好信息，保持原始顺序不变。
4. 输出的 id 列表长度必须与输入一致。

## 输出格式
严格输出如下 JSON，不要输出其他内容：
```json
{{
  "rerank_list": [
    {{"id": "xxx", "title": "xxx"}},
    ...
  ]
}}
```"""
